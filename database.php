<?php
// معلومات الخادم وقاعدة البيانات
$server = "localhost"; // عنوان الخادم المحلي
$username = "root"; // اسم المستخدم الافتراضي
$password = ""; // كلمة المرور الافتراضية (قد تكون فارغة)
$dbname = "rafi2"; // اسم قاعدة البيانات التي يجب الاتصال بها
$port = 3306; // رقم منفذ قاعدة البيانات (افتراضيًا 3306)

// إنشاء اتصال بقاعدة البيانات
$db = new mysqli($server, $username, $password, $dbname, $port);

// التحقق من الاتصال بقاعدة البيانات
if ($db->connect_error) {
    // في حالة فشل الاتصال، يتم عرض رسالة الخطأ وإيقاف التشغيل
    die("فشل الاتصال: " . $db->connect_error);
}

// التحقق من وجود قاعدة البيانات وإنشائها إذا لم تكن موجودة
$sql = "CREATE DATABASE IF NOT EXISTS $dbname";
if ($db->query($sql) !== TRUE) {
    echo "خطأ في إنشاء قاعدة البيانات: " . $db->error;
} else {
    echo "تم التحقق من قاعدة البيانات بنجاح.";
}

// إعادة الاتصال بقاعدة البيانات بعد التأكد من إنشائها
$db->select_db($dbname);
if ($db->error) {
    die("خطأ في تحديد قاعدة البيانات: " . $db->error);
}

// يمكنك إضافة المزيد من التعليمات البرمجية هنا للتعامل مع البيانات في قاعدة البيانات

?>
